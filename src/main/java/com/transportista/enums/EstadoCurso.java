package com.transportista.enums;

/**
 * Estados posibles de un curso online.
 * - PENDIENTE: recién creado, aún no publicado
 * - PUBLICADO: disponible para inscripciones
 * - CERRADO: finalizado, no acepta más inscripciones
 */
public enum EstadoCurso {

    PENDIENTE,
    PUBLICADO,
    CERRADO
}
